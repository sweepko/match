package com.sweep.pojo;

public class CrawMatch {
    private int crawId;
    private String crawTitle;
    private String crawTime;
    private String crawImage;
    private String crawContent;
    private String crawDesc;
    public int getCrawId() {
        return crawId;
    }

    public void setCrawId(int crawId) {
        this.crawId = crawId;
    }

    public String getCrawTitle() {
        return crawTitle;
    }

    public void setCrawTitle(String crawTitle) {
        this.crawTitle = crawTitle;
    }

    public String getCrawTime() {
        return crawTime;
    }

    public void setCrawTime(String crawTime) {
        this.crawTime = crawTime;
    }

    public String getCrawImage() {
        return crawImage;
    }

    public void setCrawImage(String crawImage) {
        this.crawImage = crawImage;
    }

    public String getCrawContent() {
        return crawContent;
    }

    public void setCrawContent(String crawContent) {
        this.crawContent = crawContent;
    }

    public String getCrawDesc() {
        return crawDesc;
    }

    public void setCrawDesc(String crawDesc) {
        this.crawDesc = crawDesc;
    }
}
